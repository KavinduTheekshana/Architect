    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request, [
            'image' => ['required'],
            'order' => ['string'],
        ]);
        $service = new Service();
        $service->order = $request['order'];


        if ($request->hasFile('image')) {
            $image = $request->file('image') ;
            $destinationPath = 'uploads/service/'; // upload path
            $cover_image = 'uploads/service/' . date('YmdHis') . "_" . $this->generateRandomString(10) . "_" . $image->getClientOriginalName() . "." . $image->getClientOriginalExtension();
            $image->move($destinationPath, $cover_image);
            $service->image = "$cover_image";
        }


        $service->save();
        return redirect()->back()->with('status', 'Service Image Added Sucessfully.');
    }


    function generateRandomString($length)
    {
        $characters = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';
        $charactersLength = strlen($characters);
        $randomString = '';
        for ($i = 0; $i < $length; $i++) {
            $randomString .= $characters[rand(0, $charactersLength - 1)];
        }
        return $randomString;
    }

